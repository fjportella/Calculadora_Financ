    private void jBtn_SacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_SacActionPerformed
        // TODO add your handling code here:

        if (txt_vrFinanc.getText().isEmpty() || txt_TaxaFinanc.getText().isEmpty()
                || txt_TempoFinanc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos não podem estar vazios");
        } else {

            CalcSacObj = new Calc_Amortiz();
//        CalculoObj.vrpres = Double.parseDouble(txt_vp.getText());
            CalcSacObj.setVrpres(Double.parseDouble((txt_vrFinanc.getText()).replaceAll(",", ".")));
            CalcSacObj.setTaxa(Double.parseDouble((txt_TaxaFinanc.getText()).replaceAll(",", ".")));
            CalcSacObj.setTempo(Integer.parseInt((txt_TempoFinanc.getText()).replaceAll(",", ".")));
            CalcSacObj.setAmortiz(Double.parseDouble((txt_vrFinanc.getText()).replaceAll(",", ".")) / Double.parseDouble((txt_TempoFinanc.getText()).replaceAll(",", ".")));
            //           CalcSacObj.setAmortiz(CalcSacObj.getVrparc()/Double.parseDouble(CalcSacObj.getTempo()));

            CalculoSac();

        }
    }//GEN-LAST:event_jBtn_SacActionPerformed

