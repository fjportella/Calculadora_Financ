    DecimalFormat df = new DecimalFormat("0.##");

    int lin, ncol, i, j;

    public void CalculoSac() {

        String[] cols = {"n.Parc", "SaldoIni", "Juros", "Amort", "Vr.Parc", "SaldoFim"};

//                col = Cabecalho[0].length;
        lin = CalcSacObj.getTempo();
        int ncol = cols.length;

//        double[][] calculo = new double[lin][ncol];
        String[][] calculo = new String[lin][ncol];
        //String[][] Cabecalho = new String[lin][col];

        for (i = 0; i < lin; i++) {

            CalcSacObj.CalcSac();

            for (j = 0; j < ncol; j++) {
                switch (j) {
                    case 0:
                        calculo[i][j] = Integer.toString(CalcSacObj.getNumparc());
                        break;

                    case 1:
                        calculo[i][j] = df.format(CalcSacObj.getVrpres());
                        break;

                    case 2:
                        //calculo[i][j] = Double.toString(CalcSacObj.getJuros());
                        calculo[i][j] = df.format(CalcSacObj.getJuros());
                        break;

                    case 3:
                        // calculo[i][j] = Double.toString(CalcSacObj.getAmortiz());
                        calculo[i][j] = df.format(CalcSacObj.getAmortiz());
                        break;

                    case 4:
                        //    calculo[i][j] = Double.toString(CalcSacObj.getVrparc())
                        calculo[i][j] = df.format(CalcSacObj.getVrparc());
                        break;

                    case 5:
                        //    calculo[i][j] = Double.toString(CalcSacObj.getSaldofim());
                        calculo[i][j] = df.format(CalcSacObj.getSaldofim());
                        break;

                    default:
                        System.out.print("Coluna não encontrada!");
                        break;
                }

            }
            CalcSacObj.setVrpres(CalcSacObj.getSaldofim());
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setDataVector(calculo, cols);

    }


